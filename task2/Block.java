package task2;

public class Block {
private long begin;
private long end;
public Block(long begin, long end) {
	super();
	this.begin = begin;
	this.end = end;
}
public Block() {
	super();
}
public long getBegin() {
	return begin;
}
public void setBegin(long begin) {
	this.begin = begin;
}
public long getEnd() {
	return end;
}
public void setEnd(long end) {
	this.end = end;
}

}
